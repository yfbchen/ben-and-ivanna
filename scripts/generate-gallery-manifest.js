#!/usr/bin/env node
/**
 * Scans public/gallery for images and generates a manifest.
 * Run before build so GalleryPage can import the list.
 */
import { mkdirSync, readdirSync, writeFileSync } from "fs";
import { join, dirname } from "path";
import { fileURLToPath } from "url";

const __dirname = fileURLToPath(new URL(".", import.meta.url));
const galleryDir = join(__dirname, "../public/gallery");
const outputPath = join(__dirname, "../src/generated/gallery-images.ts");

const imageExtRe = /\.(png|jpg|jpeg|webp)$/i;

let files = [];
try {
  files = readdirSync(galleryDir).filter((f) => imageExtRe.test(f));
} catch (err) {
  console.warn("Gallery directory not found, using empty list:", err.message);
}

const paths = files
  .sort()
  .map((f) => `/gallery/${f.replace(/ /g, "%20")}`);

const content = `// Auto-generated by scripts/generate-gallery-manifest.js - do not edit
export const galleryImages: string[] = ${JSON.stringify(paths, null, 2)};
`;

mkdirSync(dirname(outputPath), { recursive: true });
writeFileSync(outputPath, content, "utf8");
console.log(`Generated gallery manifest with ${paths.length} images`);
